public class DataStructure {
    private String mainName, subName;
    DataStructure(String mainName, String subName){
        this.mainName = mainName;
        this.subName = subName;
    }

    public String getMainName() {
        return mainName;
    }

    public String getSubName() {
        return subName;
    }
}
